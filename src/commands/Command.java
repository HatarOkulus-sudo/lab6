// Базовый интерфейс для реализации классов остальных команд. Содержат методы для получения имения, описания и логики выполнения команды.

package commands;

/**
 * Базовый интерфейс консольной команды.
 *
 * <p>Каждая команда предоставляет имя, описание и реализацию выполнения.</p>
 */
public interface Command {
    /**
     * Возвращает имя команды, вводимое пользователем в консоли.
     *
     * @return имя команды
     */
    String getName();

    /**
     * Возвращает краткое описание назначения команды.
     *
     * @return описание команды
     */
    String getDescription();

    /**
     * Выполняет команду с переданными аргументами.
     *
     * @param args аргументы команды
     */
    void execute(String[] args);
}
